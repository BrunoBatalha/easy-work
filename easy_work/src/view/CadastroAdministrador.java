    private void btnCadastrarAdmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarAdmActionPerformed
        try {
            AdministradorDao adao = new AdministradorDao();
            Administrador adm = new Administrador();
            adm.setNome(txtNome.getText());
            adm.setEmail(txtEmail.getText());

            String hash = gerarHash(txtSenha.getText());
            adm.setSenha(hash);

            if (adao.adicionar(adm)) {
                JOptionPane.showMessageDialog(this, "Administrador criado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Houston temos um problema!",
                        "Ops! Algo deu errado", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Falha em criar administrador: " + e.getMessage(),
                    "Mensagem de Erro", JOptionPane.ERROR_MESSAGE);
        } catch (NoSuchAlgorithmException e) {
            e.printStackTrace();
            Logger.getLogger(CadastroAdministrador.class.getName()).log(Level.SEVERE, null, e);
        } catch (UnsupportedEncodingException e) {
            e.printStackTrace();
            Logger.getLogger(CadastroAdministrador.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnCadastrarAdmActionPerformed


